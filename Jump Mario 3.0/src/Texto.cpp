#include "Texto.h"

//LIBRERIAS DE SFML
#include <SFML/Graphics.hpp>
#include<SFML/Audio.hpp>

using namespace sf;

Texto::Texto()
{
    fuente1=new Font;
    fuente1->loadFromFile("Fuentes/smb.ttf");
    text=new Text;
    text->setFont(*fuente1);
}

///////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////

void Texto::mens_tam_pos(std::string Mensaje,int Tamanio,int x, int y)
{
    text->setString(Mensaje);
    text->setCharacterSize(Tamanio);
    text->setPosition(x,y);
}

///////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////

Text Texto::dev_text()
{
    return *text;
}

///////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////

Texto::~Texto()
{
    delete fuente1;
    delete text;//dtor
}
