void Puntaje::Puntos(int &plat,float &goom,float &koop)
{
    puntaje_alt= plat+goom +koop ;

    std::stringstream ss;
    ss<<puntaje_alt;
    std::string puntaje_alt=ss.str();

    std::string punt_pant="Su puntaje es: "+ puntaje_alt ;

    punt=new Texto;
    punt->mens_tam_pos(punt_pant,38,10,20);
}

