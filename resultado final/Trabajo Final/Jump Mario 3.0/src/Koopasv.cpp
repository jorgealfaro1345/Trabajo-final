void Koopasv::Imagen_sprite()
{
    txt_enemigo->loadFromFile("Imagenes/enemigos/Koopa volador/Koopav1.png");
    spr_enemigo->setTexture(*txt_enemigo);
}

///////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////

void Koopasv::Movimiento()
{
    vel_y+=0.1;
    ey+=vel_y;
    if(ey>=mx)
    {
        vel_y=-4;
    }
}

///////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////

void Koopasv::pos_enem(std::vector <Enemigos *> enemigos)
{
    srand(time(NULL));

    for (int i=4;i<8;i++)
    {
        enemigos[i]->ex = (rand() % 8)*58 /*(rand() % 493) +i*5*/;
        enemigos[i]->ey = (rand() %4+1)* 166;
        mx=enemigos[i]->ey+10;
    }


}

///////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////

void Koopasv::Colision(int &px, int &py, float &pdx, int* &vidas)
{
    if((px + 50 > ex) && (px + 25 < ex + 58)
    && (py + 73 > ey) && (py + 73 < ey + 11) && (pdx > 0))
    {
        std::cout<<"koopas: "<<*puntos_enem<<std::endl;
        *puntos_enem+=2;
        pdx = -10;
    }

    else if((px + 50 > ex+11) && (px + 25 < ex + 58)
    && (py + 73 > ey+11) && (py + 73 < ey + 83) && (py < ey + 83))
    {
        *vidas-=1;
        std::cout<<*vidas<<std::endl;
    }
}

///////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////

Sprite Koopasv::devol_spr()
{
    if(ex<300)
    {
        txt_enemigo->loadFromFile("Imagenes/enemigos/Koopa volador/Koopav1.png");
    }
    else if(ex>300)
    {
        txt_enemigo->loadFromFile("Imagenes/enemigos/Koopa volador/Koopav2.png");
    }
    return *spr_enemigo;
}

